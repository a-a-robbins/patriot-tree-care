import { writeFileSync } from 'node:fs';
import { join } from 'node:path';

const apiBaseUrl = (process.env.QUOTE_API_URL ?? '').trim().replace(/\/$/, '');

const contents = `/** Generated by scripts/write-pages-env.mjs — do not edit by hand */
export const environment = {
  production: true,
  apiBaseUrl: ${JSON.stringify(apiBaseUrl)},
};
`;

const target = join(process.cwd(), 'src', 'environments', 'environment.pages.ts');
writeFileSync(target, contents, 'utf8');

console.log(
  apiBaseUrl
    ? `[write-pages-env] Quote API: ${apiBaseUrl}`
    : '[write-pages-env] No QUOTE_API_URL — quote form disabled on GitHub Pages',
);
